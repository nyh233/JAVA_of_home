package 测试;

public class while循环 {
    public static void main(String[] args) {
        for (int i = 1;i <= 5; i++) {
            System.out.println("HolleWorde");
        }
        System.out.println("------------------------------------");

        int i = 1;
        while (i <= 5) {
            System.out.println("HolleWorde");
            i++;
        }
    }
}
